<script>
import pathUtil from '../../../utils/pathAnimate.js';
export default {
  watch: {
    '$store.state.end': {
      handler(newValue, oldValue) {
        this.pathAnimate(newValue, oldValue);
      },
      deep: true
    },
    '$store.state.pages': (pages) => {
      let pathList = $('#end-svg path');
      pathUtil.correctEndPath(pathList[0], pathList.slice(1, 7), pages, 1.5, 0.4);
      pathUtil.correctStartPath(pathList[13], pathList.slice(7, 13), pages, 2, 0.3);
    }
  },
  methods: {
    pathAnimate(newValue, oldValue) {
        let insidePercent;
        let height = $(window).height();
        let percent = ($(window).scrollTop() % height) / height;   // 计算翻页时候翻过当前页面的百分之几
        let pathList = $('#end-svg path');
        if (percent >= 0.5 && percent <= 1) {
          insidePercent = pathUtil.getEndInsidePer(percent, 0.9);
          if (percent < 0.9) {
            // 这条线需要动画是反过来的，这是设计师画的方向总是从灯座发起的
            pathUtil.lineAnimate(pathList[0], insidePercent, true);
          } else {
            pathUtil.socketAnimate(pathList.slice(1, 7), insidePercent, false);
          }
        } else {
          insidePercent = pathUtil.getStartInsidePer(percent, 0.3);
          if (percent < 0.3) {
            pathUtil.socketAnimate(pathList.slice(7, 13), insidePercent, false);
          } else {
            pathUtil.lineAnimate(pathList[13], insidePercent, false);
          }
        }
    }
  }
}
</script>

